ASK_ACTION, ASK_QUESTION, ASK_ANSWER = range(3)

def bible_start(update: Update, context: CallbackContext) -> int:
    update.message.reply_text("–í–≤–µ–¥–∏—Ç–µ 'add' –¥–ª—è –¥–æ–±–∞–≤–ª–µ–Ω–∏—è –Ω–æ–≤–æ–π –ø–∞—Ä—ã –≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç, –∏–ª–∏ 'cancel' –¥–ª—è –æ—Ç–º–µ–Ω—ã.")
    return ASK_ACTION

def ask_action(update: Update, context: CallbackContext) -> int:
    action = update.message.text.strip().lower()
    if action == "add":
        context.user_data['action'] = 'add'
        update.message.reply_text("–í–≤–µ–¥–∏—Ç–µ –Ω–æ–≤—ã–π –≤–æ–ø—Ä–æ—Å:")
        return ASK_QUESTION
    else:
        update.message.reply_text("–ù–µ–≤–µ—Ä–Ω–æ–µ –∑–Ω–∞—á–µ–Ω–∏–µ. –í–≤–µ–¥–∏—Ç–µ 'add' –∏–ª–∏ 'cancel'.")
        return ASK_ACTION

def ask_question(update: Update, context: CallbackContext) -> int:
    question = update.message.text.strip()
    context.user_data['question'] = question
    update.message.reply_text("–í–≤–µ–¥–∏—Ç–µ –æ—Ç–≤–µ—Ç –¥–ª—è —ç—Ç–æ–≥–æ –≤–æ–ø—Ä–æ—Å–∞:")
    return ASK_ANSWER

def ask_answer(update: Update, context: CallbackContext) -> int:
    answer = update.message.text.strip()
    question = context.user_data.get('question')
    logger.info(f"–°–æ—Ö—Ä–∞–Ω—è–µ–º –ø–∞—Ä—É: –í–æ–ø—Ä–æ—Å: {question} | –û—Ç–≤–µ—Ç: {answer}")
    update.message.reply_text("–ü–∞—Ä–∞ –≤–æ–ø—Ä–æ—Å-–æ—Ç–≤–µ—Ç —Å–æ—Ö—Ä–∞–Ω–µ–Ω–∞ —Å –æ—Ç–º–µ—Ç–∫–æ–π 'Check'.")
    return ConversationHandler.END

def cancel_bible(update: Update, context: CallbackContext) -> int:
    update.message.reply_text("–û–ø–µ—Ä–∞—Ü–∏—è –æ—Ç–º–µ–Ω–µ–Ω–∞.")
    return ConversationHandler.END

conv_handler = ConversationHandler(
    entry_points=[CommandHandler('bible', bible_start)],
    states={
        ASK_ACTION: [MessageHandler(Filters.text & ~Filters.command, ask_action)],
        ASK_QUESTION: [MessageHandler(Filters.text & ~Filters.command, ask_question)],
        ASK_ANSWER: [MessageHandler(Filters.text & ~Filters.command, ask_answer)],
    },
    fallbacks=[CommandHandler('cancel', cancel_bible)]
)
# –†–µ–≥–∏—Å—Ç—Ä–∏—Ä—É–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫ –∫–æ–º–∞–Ω–¥—ã /bible –≤ –¥–∏—Å–ø–µ—Ç—á–µ—Ä–µ Telegram
dispatcher.add_handler(conv_handler)

